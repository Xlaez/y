  def search(query) do
    q = String.downcase(String.trim(query))
    keywords()
    |> Enum.filter(fn {word, _} -> String.contains?(word, q) end)
    |> Enum.flat_map(fn {_, emojis} -> emojis end)
    |> Enum.uniq()
    |> Enum.take(40)
  end

